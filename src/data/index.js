// SLOTS[tutorId][dayIndex][hourIndex] = 0 (free) | 1 (booked) — omitted = unavailable
function makeGrid(entries) {
  const g = {};
  entries.forEach(({ day, hour, booked }) => {
    if (!g[day]) g[day] = {};
    g[day][hour] = booked ? 1 : 0;
  });
  return g;
}

